package ictk.boardgame;

/* Piece ********************************************************************/
/** A place holder class for things like Chess pieces, Go Stones, Scrabble
 *  tiles etc.  This isn't well defined yet because I'm not sure what
 *  the pieces would have in common -- besides being pieces.
 */
public abstract class Piece {

   /** return the Board that this Piece is associated with (though not
    *  necessarily on at the time of the call).
    */
   public abstract Board getBoard ();
}
